/**
 * Get translation for a key based on current language
 * @param {string} key - Translation key
 * @param {string} lang - Language code (id, en, jp, kr, ar)
 * @returns {string} Translated text or key if not found
 */
export function t(key, lang = "id") {
  const translation = translations[lang]?.[key];

  if (!translation) {
    console.warn(`Translation missing for key "${key}" in language "${lang}"`);
    return key;
  }

  return translation;
}

/**
 * Get all translations for a specific language
 * @param {string} lang - Language code
 * @returns {object} All translations for the language
 */
export function getTranslations(lang = "id") {
  return translations[lang] || translations.id;
}

/**
 * Check if language is RTL (Right-to-Left)
 * @param {string} lang - Language code
 * @returns {boolean} True if RTL language
 */
export function isRTL(lang) {
  return lang === "ar";
}

/**
 * Get language direction
 * @param {string} lang - Language code
 * @returns {string} 'rtl' or 'ltr'
 */
export function getDirection(lang) {
  return isRTL(lang) ? "rtl" : "ltr";
}
